#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

int main()
{
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	{ //check for special case null weapon for HumanB
		HumanB tom("Tom");
		tom.attack();
		Weapon bat("Baseball bat");
		tom.setWeapon(bat);
		tom.attack();
	}
	return 0;
}
